// Activity Tracker - log all user activities to localStorage
function logActivity(type, description, details = {}) {
    const currentUserIndex = Number(localStorage.getItem('currentUserIndex'));
    if (currentUserIndex < 0) return;

    const usersDatabase = JSON.parse(localStorage.getItem('AprilUsers')) || [];
    const user = usersDatabase[currentUserIndex];
    
    if (!user) return;

    // Initialize activities array if not exists
    if (!user.activities) {
        user.activities = [];
    }

    // Create activity object
    const activity = {
        id: Date.now(),
        type: type,
        description: description,
        timestamp: new Date().toISOString(),
        details: details
    };

    // Add to beginning of array (most recent first)
    user.activities.unshift(activity);

    // Keep only last 100 activities
    if (user.activities.length > 100) {
        user.activities = user.activities.slice(0, 100);
    }

    localStorage.setItem('AprilUsers', JSON.stringify(usersDatabase));
}

// Log transaction (purchase)
function logTransaction(items, totalAmount) {
    const currentUserIndex = Number(localStorage.getItem('currentUserIndex'));
    if (currentUserIndex < 0) return;

    const usersDatabase = JSON.parse(localStorage.getItem('AprilUsers')) || [];
    const user = usersDatabase[currentUserIndex];
    
    if (!user) return;

    if (!user.transactions) {
        user.transactions = [];
    }

    const transaction = {
        id: Date.now(),
        items: items,
        totalAmount: totalAmount,
        status: 'completed',
        date: new Date().toISOString(),
        itemCount: items.reduce((sum, item) => sum + item.quantity, 0)
    };

    user.transactions.unshift(transaction);
    
    // Log as activity too
    logActivity('purchase', `Purchased ${items.length} item(s) for $${totalAmount.toFixed(2)}`, {
        itemCount: transaction.itemCount,
        totalAmount: totalAmount,
        transactionId: transaction.id
    });

    localStorage.setItem('AprilUsers', JSON.stringify(usersDatabase));
}

// Get activities
function getActivities(limit = 10) {
    const user = getUser();
    if (!user || !user.activities) return [];
    return user.activities.slice(0, limit);
}

// Get transactions
function getTransactions(limit = 10) {
    const user = getUser();
    if (!user || !user.transactions) return [];
    return user.transactions.slice(0, limit);
}

// Get total items bought
function getTotalItemsBought() {
    const user = getUser();
    if (!user || !user.transactions) return 0;
    
    return user.transactions.reduce((total, transaction) => {
        return total + transaction.itemCount;
    }, 0);
}

// Get pending cart items count
function getPendingCartCount() {
    return getCart().reduce((total, item) => total + item.quantity, 0);
}

// Get pending cart total
function getPendingCartTotal() {
    return getCart().reduce((total, item) => total + (item.price * item.quantity), 0);
}

// Format time difference
function getTimeAgo(timestamp) {
    const now = new Date();
    const date = new Date(timestamp);
    const seconds = Math.floor((now - date) / 1000);

    if (seconds < 60) return 'just now';
    const minutes = Math.floor(seconds / 60);
    if (minutes < 60) return `${minutes}m ago`;
    const hours = Math.floor(minutes / 60);
    if (hours < 24) return `${hours}h ago`;
    const days = Math.floor(hours / 24);
    if (days < 7) return `${days}d ago`;
    const weeks = Math.floor(days / 7);
    if (weeks < 4) return `${weeks}w ago`;
    
    return date.toLocaleDateString();
}

